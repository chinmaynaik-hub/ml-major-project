def get_pose_pronunciation(pose_name: str) -> str:
    """
    Get the phonetic pronunciation for a Sanskrit pose name
    
    Args:
        pose_name: Sanskrit pose name from dataset
    
    Returns:
        Phonetic pronunciation with English description
    """
    return POSE_PRONUNCIATION_MAP.get(pose_name.lower(), pose_name)

def get_all_pose_names():
    """Get all available pose names"""
    return list(POSE_PRONUNCIATION_MAP.keys())

def generate_voice_text(pose_name: str, feedback: str = "") -> str:
    """
    Generate text for TTS that includes proper Sanskrit pronunciation
    
    Args:
        pose_name: Sanskrit pose name
        feedback: Optional feedback text
    
    Returns:
        Complete text for TTS
    """
    pronunciation = get_pose_pronunciation(pose_name)
    
    if feedback:
        return f"Move into {pronunciation}. {feedback}"
    else:
        return f"Perfect! You are in {pronunciation}"

